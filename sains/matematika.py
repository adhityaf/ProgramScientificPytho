# Fungsi untuk melakukan operasi tambah
def tambah(a, b):
    print(f"Hasil Operasi Tambah {a} + {b} \t= {a + b}")

# Fungsi untuk melakukan operasi kurang
def kurang(a, b):
    print(f"Hasil Operasi Kurang {a} - {b}\t= {a - b}")

# Fungsi untuk melakukan operasi kali
def kali(a, b):
    print(f"Hasil Operasi Kali {a} * {b}\t= {a * b}")

# Fungsi untuk melakukan operasi bagi
def bagi(a, b):
    print(f"Hasil Operasi bagi {a} / {b}\t= {a / b}")  